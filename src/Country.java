import java.util.HashMap;

public class Country {
	private String name;
	private String link;
	

	public Country() {
		super();
	}


	public Country(String name, String link) {
		super();
		this.name = name;
		this.link = link;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getLink() {
		return link;
	}


	public void setLink(String link) {
		this.link = link;
	}
	
		
	
	
}
